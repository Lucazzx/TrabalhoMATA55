    public void iniciaCalendario(){
        SimpleDateFormat Ano = new SimpleDateFormat("yyyy"); 
        SimpleDateFormat Mes = new SimpleDateFormat("MM"); 
        SimpleDateFormat Dia = new SimpleDateFormat("dd"); 
        
        anoFixo = Integer.parseInt(Ano.format (new Date()));
        mesFixo = Integer.parseInt(Mes.format (new Date()));
        diaFixo = Integer.parseInt(Dia.format (new Date()));
        
        //JOptionPane.showMessageDialog(null, diaFixo+"/"+mesFixo+"/"+anoFixo);
        
        int anoMaximo = anoFixo +50;
        for (int i = 2000; i< anoMaximo; i++){
            selecaoAno.addItem(String.valueOf(i));
        }
        selecaoAno.setSelectedItem(anoFixo);
        
        switch (mesFixo) {
            case 1 -> selecaoMes.setSelectedIndex(0);
            case 2 -> selecaoMes.setSelectedIndex(1);
            case 3 -> selecaoMes.setSelectedIndex(2);
            case 4 -> selecaoMes.setSelectedIndex(3);
            case 5 -> selecaoMes.setSelectedIndex(4);
            case 6 -> selecaoMes.setSelectedIndex(5);
            case 7 -> selecaoMes.setSelectedIndex(6);
            case 8 -> selecaoMes.setSelectedIndex(7);
            case 9 -> selecaoMes.setSelectedIndex(8);
            case 10 -> selecaoMes.setSelectedIndex(9);
            case 11 -> selecaoMes.setSelectedIndex(10);
            case 12 -> selecaoMes.setSelectedIndex(11);
            default -> {
            }
        }
    }
    
