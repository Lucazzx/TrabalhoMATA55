    public void verificaSemana(){
        
        if (mesAltera != mesFixo-1 & diaClick !=0){
            diaAltera = 1;
        }
        if (anoAltera != anoFixo & diaClick !=0){
            diaAltera = 1;
        }
        diaClick = 1;
        
        if(diaAltera == diaFixo & mesAltera +1 == mesFixo & anoAltera == anoFixo){
            diaPagina.setForeground(Color.red);
        } else {
            diaPagina.setForeground(Color.black);
        }
        
        mesAltera = selecaoMes.getSelectedIndex();
        anoAltera = Integer.parseInt(selecaoAno.getSelectedItem().toString());
        
        Calendar calendarN = Calendar.getInstance();
        calendarN.set (anoAltera, mesAltera, diaAltera);
        
        mesPagina.setText(selecaoMes.getSelectedItem().toString());
        diaPagina.setText(""+diaAltera);
        
        int semanaN = calendarN.get(Calendar.DAY_OF_WEEK);
        switch (semanaN) {
            case Calendar.SUNDAY -> semanaPagina.setText("Domingo");
            case Calendar.MONDAY -> semanaPagina.setText("Segunda");
            case Calendar.TUESDAY -> semanaPagina.setText("Terça");
            case Calendar.WEDNESDAY -> semanaPagina.setText("Quarta");
            case Calendar.THURSDAY -> semanaPagina.setText("Quinta");
            case Calendar.FRIDAY -> semanaPagina.setText("Sexta");
            case Calendar.SATURDAY -> semanaPagina.setText("Sábado");
            default -> {
            }
        }
    }
    
    public void eventosMouse(javax.swing.JLabel a){
       
        a.addMouseListener(new java.awt.event.MouseAdapter() {
           public void mouseEntered(java.awt.event.MouseEvent evt){
               if (!"".equals(a.getText())){
                   a.setBorder(javax.swing.BorderFactory.createBevelBorder(javax.swing.border.BevelBorder.RAISED));
               }
           }
           public void mouseExited(java.awt.event.MouseEvent evt){
               a.setBorder(null);
           } 
           public void mouseClicked(java.awt.event.MouseEvent evt){
               if (!"".equals(a.getText())){
                   diaPagina.setText(a.getText());
                   diaAltera = Integer.parseInt(a.getText());
                   diaClick = 0;
                   verificaSemana();
                   //leAgenda();
               }
           }
        });
         
    }
        
